//операторы присваивания
let age = 18+5;
age +=2;//age = age+2
age -=3;//age = age-3
age *=2;//age = age*2
age /=2;//age = age/2
console.log(age);
//операторы сравнения
const vasya = 20;
console.log(age > vasya);
console.log(age >= vasya);
console.log(age < vasya);
console.log(age <= vasya);
console.log(age == vasya);